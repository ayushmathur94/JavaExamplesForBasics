package coreBasics;

public class ConditionalTernaryOperators {

	private static int hyundai10TyreSize = 16 ;
	
	public static void main(String[] args) {
		
		System.out.println(hyundai10TyreSize <= 16  ? "Size is okay for city driving!!" : "Size is small for off road adventures.");
		
	}
	
	/* Ternary operator is like smaller version of if else. it is denoted by symbols ? and :
	 * we write direct the condition first and for true, we use " ? " followed by statment to execute if true  
	 * and for false (i.e. else part), we use " : " followed by statement to execute if false.
	 */
	
}
