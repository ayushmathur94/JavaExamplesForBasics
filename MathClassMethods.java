package coreBasics;

public class MathClassMethods {
 
	public static void main(String[] args) {
	
		// Math.abs(arg)
		System.out.println("Giving Absolute Method : " + Math.abs(-7.8));
		
		//Math.ceil(arg)
		System.out.println("Giving ceil value i.e. value up the number line : " + Math.ceil(6.2));
		System.out.println("Giving ceil value i.e. value up the number line : " + Math.ceil(-6.2));
		
		//Math.floor(args)
		System.out.println("Giving floor value i.e. value down the number line : " + Math.floor(5.9));
		System.out.println("Giving floor value i.e. value down the number line : " + Math.floor(-5.9));
	
		//Math.max((arg1),(arg2))
		System.out.println("Giving max value of the two input numbers : " + Math.max(5.6, 7.6));
		System.out.println("Giving max value of the two input numbers : " + Math.max(-7.6, -6.6));
	 
		//Math.min(arg1, arg2)
		System.out.println("Giving min value of the two input numbers : " + Math.min(11.8, 2.2));
	    System.out.println("Giving min value of the two input numbers : " + Math.min(-33.4, -1.7));
		
	    //Math.pow(arg1, arg2)
	    System.out.println("Giving value of input values after applying power : " + Math.pow(4, 3));
		
	    //Math.sqrt(arg1)
	    System.out.println("Giving square root of the input value : " + Math.sqrt(64));
	}
	
	
      
	
	
 
	
	
}
