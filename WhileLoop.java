package coreBasics;

public class WhileLoop {

	public static void main(String[] args) {
/* While loop 
 * allow you to execute a certain block of code multiple times , but we have to only write it(in the while loop)  one time.		
 */
		
		int carQuant = 0;
		
		while (carQuant <= 15){
			System.out.println("CAR Quantity in garage : " + carQuant);
		carQuant++ ;
		}
		
		
	}
	
}
